var zhEns = document.querySelectorAll('.zh-en');

var lang = localStorage.getItem('lang') || 'zh';

var zhImgs = document.querySelectorAll('.zh');
var enImgs = document.querySelectorAll('.en');

var langZh = document.querySelector('.lang-zh');
var langZh2 = document.querySelector('.lang-zh2');
var langEn = document.querySelector('.lang-en');
var langEn2 = document.querySelector('.lang-en2');

var v750header = document.querySelector('#v750header');

var index = 0;
v750header.onclick = function() {
  if (index++ % 2 == 0) {
    v750header.setAttribute('show', true);
  } else {
    v750header.removeAttribute('show');
  }
};

function changeLang(_lang, v750) {
  if (!v750header.getAttribute('show') && v750) {
    return;
  }

  lang = _lang;
  localStorage.setItem('lang', lang);

  for (var i = 0; i < zhEns.length; i++) {
    var dom = zhEns[i];
    var text = dom.innerText || dom.getAttribute('placeholder');
    var enText;

    if (lang == 'en') {
      enText = translate[text];
    } else {
      enText = findKey(text);
    }
    if (enText) {
      if (dom.getAttribute('placeholder')) {
        dom.setAttribute('placeholder', enText);
      } else {
        dom.innerText = enText;
      }
    }

    if (lang == 'en') {
      zhImgs.forEach(function(item) {
        item.setAttribute('hide', true);
        ;
      });
      enImgs.forEach(function(item) {
        item.removeAttribute('hide');
      });
      langZh.removeAttribute('active');
      langEn.setAttribute('active', true);
      langZh2.removeAttribute('active');
      langEn2.setAttribute('active', true);
    } else if (lang == 'zh') {
      zhImgs.forEach(function(item) {
        item.removeAttribute('hide');
      });
      enImgs.forEach(function(item) {
        item.setAttribute('hide', true);
      });
      langZh.setAttribute('active', true);
      langEn.removeAttribute('active');
      langZh2.setAttribute('active', true);
      langEn2.removeAttribute('active');
    }
  }
}

